                var name = $("#name").val();
                var email = $("#email").val();

                var errors = [];

                // Validation du nom
                if (name === "" || hasConsecutiveThreeLetters(name)) {
                    errors.push("Veuillez corriger votre nom.");
                }

                // Validation de l'email
                if (email === "" || !isValidEmail(email) || countDigits(email) > 8) {
                    errors.push("Veuillez corriger votre adresse email.");
                }

                // Afficher les erreurs s'il y en a
                if (errors.length > 0) {
                    alert(errors.join("\n"));
                    return false;
                }

                // Si tout est valide, soumettre le formulaire
                document.getElementById("signupForm").submit();
            });

            function hasConsecutiveThreeLetters(str) {
                return /([a-zA-Z])\1\1/.test(str);
            }

            function isNumericOnly(str) {
                return /^[0-9]+$/.test(str);
            }

            function isSpecialCharsOnly(str) {
                return /^[^a-zA-Z0-9]+$/.test(str);
            }

            function isValidEmail(email) {
                return /\S+@\S+\.\S+/.test(email);
            }

            function countDigits(str) {
                var digitCount = str.replace(/\D/g, "").length;
                return digitCount;
            }
        });
