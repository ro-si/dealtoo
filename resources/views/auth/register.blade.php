                function updateTotalCount(totalPostsCount, messagesCount) {
                    var totalCount = totalPostsCount + messagesCount;
                    document.getElementById('total-count-badge').textContent = totalCount;
                    document.getElementById('total-message').textContent = messagesCount;
                }

                function checkNewMessages() {
                    let oldValue = $('.count-threads-with-new-messages-notif').html();
                    let messageNotif = $('.count-threads-with-new-messages');

                    if (typeof oldValue === 'undefined') {
                        return false;
                    }

                    /* Make ajax call */
                    let ajax = $.ajax({
                        method: 'POST',
                        url: siteUrl + '/account/messages/check-new',
                        data: {
                            'languageCode': languageCode,
                            'oldValue': oldValue,
                            '_token': $('input[name=_token]').val()
                        }
                    });

                    ajax.done(function(data) {
                        if (typeof data.logged === 'undefined') {
                            return false;
                        }

                        /* Guest Users - Need to Log In */
                        if (data.logged === 0 || data.logged === '0' || data.logged === '') {
                            return false;
                        }

                        /* Update the total count with new messages */
                        let messagesCount = 0;
                        if (data.countThreadsWithNewMessages > 0) {
                            if (data.countThreadsWithNewMessages >= data.countLimit) {
                                messagesCount = data.countLimit;
                            } else {
                                messagesCount = data.countThreadsWithNewMessages;
                            }
                        }

                        // Update the total count with the new message count
                        updateTotalCount(0, messagesCount);

                        return false;
                    });
                }

                // Initial call to update the total count
                updateTotalCount(0, 0);

                // Set interval to check for new messages
                setInterval(checkNewMessages, 5000);
            });
